/**
 * SPEC-042: fusión de las dos secciones «Objetivos» — la sección superior es
 * la superficie ÚNICA de estado Y edición: fila por objetivo (icono de estado
 * en vivo + Input editable + lápiz + Eliminar), draft con Guardar/Descartar
 * (patrón SPEC-029: barra solo con cambios, descartar SIEMPRE con
 * AlertDialog), Toast «Objetivos guardados», filtrado de vacíos, añadir en
 * lista y en empty state, invalidación de evaluación/marcas al guardar
 * (invariante del repositorio, aquí simulada por el mock de updateInterview),
 * estados previos conservados (motivos, marca manual, lápiz, Evaluar
 * objetivos) y sección Guión sin ningún bloque de objetivos (derogación de
 * SPEC-014 AC de edición / SPEC-029).
 * Generated by QA Dev agent.
 *
 * Arnés de SPEC-025/028 (ObjectivesSection montada vía InterviewDetailPage,
 * frontera de mocking = window.api con installMockApi; seguimiento en vivo
 * con emitAssistantUpdate) + interacciones de editor de ScriptSection.test.tsx
 * (cambios vía toolbar de TipTap: jsdom no emite beforeinput).
 */
import { act, render, screen, waitFor, within, type RenderResult } from '@testing-library/react'
import userEvent from '@testing-library/user-event'
import { beforeEach, describe, expect, it, vi } from 'vitest'
import { MemoryRouter, Route, Routes } from 'react-router-dom'
import { Toaster } from '@/components/ui/sonner'
import { TooltipProvider } from '@/components/ui/tooltip'
import { InterviewDetailPage } from '@/pages/InterviewDetailPage'
import type { Company, Interview, ObjectiveOverride } from '@/types/domain'
import { installMockApi, type MockApiHandle } from '../../helpers/mockApi'

let mockApi: MockApiHandle

const COMPANY: Company = {
  id: 'c-1',
  name: 'Acme Corp',
  website: null,
  linkedinUrl: null,
  createdAt: '2026-07-02T12:00:00.000Z',
  updatedAt: '2026-07-02T12:00:00.000Z'
}

const TRANSCRIPT_PATH = '/tmp/maurya-recordings/entrevista.transcript.json'

function interview(overrides: Partial<Interview> = {}): Interview {
  return {
    id: 'i-1',
    discoveryId: 'd-1',
    companyId: 'c-1',
    contactIds: [],
    interviewGroupId: null,
    templateId: 'tpl-1',
    title: 'Discovery con Acme',
    status: 'draft',
    scriptMarkdown: null,
    objectives: ['Objetivo cero', 'Objetivo uno'],
    wavPath: null,
    transcriptPath: null,
    createdAt: '2026-07-04T10:00:00.000Z',
    updatedAt: '2026-07-04T10:00:00.000Z',
    ...overrides
  }
}

const WITH_SCRIPT = interview({
  scriptMarkdown: '# Guión adaptado\n\n## Bloque 1\nPregunta adaptada a Acme',
  objectives: ['Objetivo A', 'Objetivo B'],
  status: 'prepared'
})

/** Entrevista con la evaluación LLM persistida: cero cumplido, uno no cumplido. */
const EVALUATED = interview({
  status: 'recorded',
  wavPath: '/tmp/maurya-recordings/entrevista.wav',
  transcriptPath: TRANSCRIPT_PATH,
  objectiveResults: [
    { met: true, reason: 'Se obtuvo el dato concreto con cifras del último trimestre.' },
    { met: false, reason: 'La conversación no llegó a tocar este tema con hechos pasados.' }
  ]
})

const OVERRIDE_MET: ObjectiveOverride = {
  met: true,
  comment: 'Solo hubo generalidades, pero el cliente firmó.',
  text: 'El gasto de 200 € culminó en una orden firmada, según el entrevistador.'
}

/** EVALUATED + marca manual sobre el objetivo 1 (no cumplido → cumplido). */
const EVALUATED_WITH_OVERRIDE = interview({
  ...EVALUATED,
  objectiveOverrides: [null, OVERRIDE_MET]
})

function setInterview(value: Interview): void {
  vi.mocked(mockApi.api.db.getInterview).mockResolvedValue({ ok: true, data: value })
}

function renderDetail(): RenderResult {
  return render(
    <TooltipProvider>
      <MemoryRouter initialEntries={['/discoveries/d-1/companies/c-1/interviews/i-1']}>
        <Routes>
          <Route
            path="/discoveries/:discoveryId/companies/:companyId/interviews/:interviewId"
            element={<InterviewDetailPage />}
          />
        </Routes>
      </MemoryRouter>
      <Toaster />
    </TooltipProvider>
  )
}

/** Monta el detalle y devuelve la sección Objetivos una vez cargada. */
async function findSection(): Promise<HTMLElement> {
  renderDetail()
  return await screen.findByTestId('objectives-section')
}

/** Valores de los Inputs de objetivos de la sección, en orden. */
function inputValues(section: HTMLElement): string[] {
  return within(section)
    .getAllByTestId('objective-input')
    .map((input) => (input as HTMLInputElement).value)
}

beforeEach(() => {
  vi.clearAllMocks()
  mockApi = installMockApi()
  vi.mocked(mockApi.api.db.getCompany).mockResolvedValue({ ok: true, data: COMPANY })
  setInterview(interview())
  vi.mocked(mockApi.api.llm.getStatus).mockResolvedValue({
    ok: true,
    data: { hasAnthropicKey: true }
  })
})

describe('ObjectivesSection (edición fusionada SPEC-042)', () => {
  describe('merged row composition', () => {
    // SPEC-042 · AC-01 (fila: icono + Input editable + lápiz + Eliminar objetivo)
    it('renders each objective with its state icon, an editable Input, the compliance pencil and a delete button', async () => {
      const section = await findSection()

      const items = within(section).getAllByTestId('objective-item')
      expect(items).toHaveLength(2)
      expect(inputValues(section)).toEqual(['Objetivo cero', 'Objetivo uno'])
      items.forEach((item, index) => {
        // Icono de estado (Target muted = pending; jsdom no aplica Tailwind →
        // se asierta por data-state, patrón SPEC-025)
        expect(item).toHaveAttribute('data-state', 'pending')
        expect(item.querySelector('svg')).not.toBeNull()
        const input = within(item).getByLabelText(`Objetivo ${index + 1}`)
        expect(input).toBeEnabled()
        expect(
          within(item).getByRole('button', { name: 'Editar cumplimiento del objetivo' })
        ).toBeInTheDocument()
        expect(within(item).getByRole('button', { name: 'Eliminar objetivo' })).toBeInTheDocument()
      })
    })

    // SPEC-042 · AC-02 (icono en vivo: el evento del asistente lo pone verde)
    it('turns the objective icon to met in real time when an assistant event marks it as covered', async () => {
      const section = await findSection()

      act(() => {
        mockApi.emitAssistantUpdate({
          state: 'active',
          queue: { pending: [], pinned: [] },
          objectivesMet: [1]
        })
      })

      const items = within(section).getAllByTestId('objective-item')
      await waitFor(() => expect(items[1]).toHaveAttribute('data-state', 'met'))
      expect(items[0]).toHaveAttribute('data-state', 'pending')
    })
  })

  describe('in-section editing', () => {
    // SPEC-042 · AC-03 (Guardar/Descartar solo ante cambios, patrón SPEC-029)
    it('shows the Guardar and Descartar buttons only after editing an objective Input', async () => {
      const user = userEvent.setup()
      const section = await findSection()
      expect(screen.queryByTestId('objectives-editor-actions')).not.toBeInTheDocument()

      await user.type(within(section).getByLabelText('Objetivo 1'), ' bis')

      const actions = await screen.findByTestId('objectives-editor-actions')
      expect(within(actions).getByRole('button', { name: 'Guardar' })).toBeInTheDocument()
      expect(within(actions).getByRole('button', { name: 'Descartar' })).toBeInTheDocument()
    })

    // SPEC-042 · AC-04 (guardar: persistencia + Toast + Inputs con lo guardado)
    it('persists the edited objectives on save showing the "Objetivos guardados" toast and hiding the action bar', async () => {
      const user = userEvent.setup()
      vi.mocked(mockApi.api.db.updateInterview).mockResolvedValue({
        ok: true,
        data: interview({ objectives: ['Objetivo cero bis', 'Objetivo uno'] })
      })
      const section = await findSection()

      await user.type(within(section).getByLabelText('Objetivo 1'), ' bis')
      const actions = await screen.findByTestId('objectives-editor-actions')
      await user.click(within(actions).getByRole('button', { name: 'Guardar' }))

      expect(vi.mocked(mockApi.api.db.updateInterview)).toHaveBeenCalledWith('i-1', {
        objectives: ['Objetivo cero bis', 'Objetivo uno']
      })
      const toasts = await screen.findAllByText('Objetivos guardados')
      expect(toasts.length).toBeGreaterThanOrEqual(1)
      await waitFor(() =>
        expect(screen.queryByTestId('objectives-editor-actions')).not.toBeInTheDocument()
      )
      expect(inputValues(section)).toEqual(['Objetivo cero bis', 'Objetivo uno'])
    })

    // SPEC-042 · AC-05 (un objetivo en blanco se descarta al guardar, SPEC-014)
    it('drops an objective edited to blank from the persisted list on save', async () => {
      const user = userEvent.setup()
      vi.mocked(mockApi.api.db.updateInterview).mockResolvedValue({
        ok: true,
        data: interview({ objectives: ['Objetivo uno'] })
      })
      const section = await findSection()

      await user.clear(within(section).getByLabelText('Objetivo 1'))
      const actions = await screen.findByTestId('objectives-editor-actions')
      await user.click(within(actions).getByRole('button', { name: 'Guardar' }))

      // El vacío no viaja en la persistencia (filtrado con trim)
      expect(vi.mocked(mockApi.api.db.updateInterview)).toHaveBeenCalledWith('i-1', {
        objectives: ['Objetivo uno']
      })
      await waitFor(() => expect(inputValues(section)).toEqual(['Objetivo uno']))
    })

    // SPEC-042 · AC-06 (Descartar: AlertDialog y vuelta a lo persistido)
    it('confirms the discard in an AlertDialog and restores the Inputs to the persisted objectives', async () => {
      const user = userEvent.setup()
      const section = await findSection()

      await user.type(within(section).getByLabelText('Objetivo 1'), ' bis')
      const actions = await screen.findByTestId('objectives-editor-actions')
      await user.click(within(actions).getByRole('button', { name: 'Descartar' }))

      const dialog = await screen.findByRole('alertdialog')
      expect(within(dialog).getByRole('heading', { name: 'Descartar cambios' })).toBeInTheDocument()
      await user.click(within(dialog).getByRole('button', { name: 'Descartar' }))

      await waitFor(() =>
        expect(screen.queryByTestId('objectives-editor-actions')).not.toBeInTheDocument()
      )
      expect(inputValues(section)).toEqual(['Objetivo cero', 'Objetivo uno'])
      expect(vi.mocked(mockApi.api.db.updateInterview)).not.toHaveBeenCalled()
    })

    // SPEC-042 · AC-07 (eliminar muta el draft; solo persiste al guardar)
    it('removes the objective from the displayed list on delete showing the action bar without persisting yet', async () => {
      const user = userEvent.setup()
      const section = await findSection()

      await user.click(within(section).getAllByRole('button', { name: 'Eliminar objetivo' })[0])

      expect(inputValues(section)).toEqual(['Objetivo uno'])
      expect(await screen.findByTestId('objectives-editor-actions')).toBeInTheDocument()
      expect(vi.mocked(mockApi.api.db.updateInterview)).not.toHaveBeenCalled()
    })

    // SPEC-042 · AC-08 (añadir: un Input vacío al final)
    it('appends an empty Input at the end of the list when Añadir objetivo is pressed', async () => {
      const user = userEvent.setup()
      const section = await findSection()

      await user.click(within(section).getByTestId('objectives-add-button'))

      expect(inputValues(section)).toEqual(['Objetivo cero', 'Objetivo uno', ''])
      expect(within(section).getByLabelText('Objetivo 3')).toHaveValue('')
    })

    // SPEC-042 · AC-09 (guardar cambios invalida evaluación y marcas — el
    // repositorio hace la invalidación real; el mock devuelve la Interview
    // sin results/overrides, como haría main)
    it('stops showing the evaluation reasons and the manual mark after saving a modified list', async () => {
      const user = userEvent.setup()
      setInterview(EVALUATED_WITH_OVERRIDE)
      vi.mocked(mockApi.api.db.updateInterview).mockResolvedValue({
        ok: true,
        data: interview({
          ...EVALUATED_WITH_OVERRIDE,
          objectives: ['Objetivo cero bis', 'Objetivo uno'],
          objectiveResults: null,
          objectiveOverrides: null
        })
      })
      const section = await findSection()
      await within(section).findAllByTestId('objective-reason')
      expect(within(section).getByTestId('objective-override-text')).toBeInTheDocument()

      await user.type(within(section).getByLabelText('Objetivo 1'), ' bis')
      const actions = await screen.findByTestId('objectives-editor-actions')
      await user.click(within(actions).getByRole('button', { name: 'Guardar' }))

      await waitFor(() =>
        expect(within(section).queryAllByTestId('objective-reason')).toHaveLength(0)
      )
      expect(within(section).queryByTestId('objective-override-text')).not.toBeInTheDocument()
    })
  })

  describe('preserved states', () => {
    // SPEC-042 · AC-10 (motivos, marca manual y lápiz siguen presentes y funcionales)
    it('keeps the evaluation reason, the rewritten mark text and a functional pencil per objective', async () => {
      const user = userEvent.setup()
      setInterview(EVALUATED_WITH_OVERRIDE)
      const section = await findSection()

      const reasons = await within(section).findAllByTestId('objective-reason')
      expect(reasons.map((reason) => reason.textContent)).toEqual([
        'Se obtuvo el dato concreto con cifras del último trimestre.',
        'La conversación no llegó a tocar este tema con hechos pasados.'
      ])
      // La marca manual tacha el motivo previo y muestra el texto reescrito
      expect(reasons[1]).toHaveAttribute('data-overridden', 'true')
      expect(within(section).getByTestId('objective-override-text')).toHaveTextContent(
        OVERRIDE_MET.text
      )
      // El lápiz sigue abriendo el diálogo de cumplimiento (SPEC-028)
      const pencils = within(section).getAllByRole('button', {
        name: 'Editar cumplimiento del objetivo'
      })
      expect(pencils).toHaveLength(2)
      await user.click(pencils[0])
      expect(await screen.findByTestId('objective-override-dialog')).toBeInTheDocument()
    })

    // SPEC-042 · AC-11 («Evaluar objetivos» sigue presente y funcional)
    it('keeps the Evaluar objetivos button working for a transcribed interview without evaluation', async () => {
      const user = userEvent.setup()
      setInterview(
        interview({
          status: 'recorded',
          wavPath: '/tmp/maurya-recordings/entrevista.wav',
          transcriptPath: TRANSCRIPT_PATH
        })
      )
      vi.mocked(mockApi.api.llm.evaluateObjectives).mockResolvedValue({
        ok: true,
        data: EVALUATED
      })
      const section = await findSection()

      // OJO (lección SPEC-029): el botón deshabilitado va envuelto en Tooltip
      // y se REMONTA al habilitarse (getStatus async) → esperar toBeEnabled
      // por testid y re-consultar antes de clicar
      await waitFor(() =>
        expect(within(section).getByTestId('objectives-evaluate-button')).toBeEnabled()
      )
      await user.click(within(section).getByTestId('objectives-evaluate-button'))

      expect(vi.mocked(mockApi.api.llm.evaluateObjectives)).toHaveBeenCalledWith('i-1')
      const toasts = await screen.findAllByText('Objetivos evaluados')
      expect(toasts.length).toBeGreaterThanOrEqual(1)
    })

    // SPEC-042 · AC-12 (empty state con «Añadir objetivo»: ahora se añaden aquí)
    it('includes the Añadir objetivo button in the empty state and adds the first Input from there', async () => {
      const user = userEvent.setup()
      setInterview(interview({ objectives: [] }))
      const section = await findSection()

      expect(await within(section).findByText('Sin objetivos')).toBeInTheDocument()
      expect(
        within(section).getByText('Se generan con el guión o añádelos aquí')
      ).toBeInTheDocument()
      const addButton = within(section).getByTestId('objectives-add-button')

      await user.click(addButton)

      // Desviación aceptada: el alta desde el empty state crea el draft con un
      // Input vacío y la barra Guardar/Descartar (lo persistido sigue vacío)
      expect(within(section).getByLabelText('Objetivo 1')).toHaveValue('')
      expect(await screen.findByTestId('objectives-editor-actions')).toBeInTheDocument()
      expect(vi.mocked(mockApi.api.db.updateInterview)).not.toHaveBeenCalled()
    })
  })

  describe('Guión section without objectives (SPEC-042 derogation)', () => {
    // SPEC-042 · AC-13 (la sección Guión ya no contiene ningún bloque «Objetivos»)
    it('renders the Guión section without any Objetivos block, heading, Inputs or add button', async () => {
      setInterview(WITH_SCRIPT)
      renderDetail()

      const guionHeading = await screen.findByRole('heading', { name: 'Guión' })
      const scriptSection = guionHeading.closest('section')
      if (scriptSection === null) {
        throw new Error('El heading «Guión» debe vivir dentro de un <section>')
      }
      await within(scriptSection).findByTestId('script-markdown-editor')
      // Ni heading (el h4 de SPEC-029 queda derogado), ni Inputs, ni añadir
      expect(
        within(scriptSection).queryByRole('heading', { name: 'Objetivos' })
      ).not.toBeInTheDocument()
      expect(within(scriptSection).queryAllByTestId('objective-input')).toHaveLength(0)
      expect(
        within(scriptSection).queryByRole('button', { name: 'Añadir objetivo' })
      ).not.toBeInTheDocument()
      // La coexistencia de DOS headings «Objetivos» desaparece: queda solo el h3
      expect(screen.getAllByRole('heading', { name: 'Objetivos' })).toHaveLength(1)
      expect(screen.getByRole('heading', { name: 'Objetivos', level: 3 })).toBeInTheDocument()
    })

    // SPEC-042 · AC-14 (guardar el guión persiste SOLO scriptMarkdown)
    it('persists only the script markdown when saving a script edit, leaving the objectives untouched', async () => {
      const user = userEvent.setup()
      setInterview(WITH_SCRIPT)
      const savedMarkdown = '### Guión adaptado\n\n## Bloque 1\n\nPregunta adaptada a Acme'
      vi.mocked(mockApi.api.db.updateInterview).mockResolvedValue({
        ok: true,
        data: { ...WITH_SCRIPT, scriptMarkdown: savedMarkdown }
      })
      renderDetail()

      // Cambio real vía toolbar (jsdom sin beforeinput): h1 → Encabezado 3
      const editor = await screen.findByTestId('script-markdown-editor')
      await user.click(within(editor).getByRole('button', { name: 'Encabezado 3' }))
      const actions = await screen.findByTestId('script-editor-actions')
      await user.click(within(actions).getByRole('button', { name: 'Guardar' }))

      expect(vi.mocked(mockApi.api.db.updateInterview)).toHaveBeenCalledWith('i-1', {
        scriptMarkdown: savedMarkdown
      })
      const payload = vi.mocked(mockApi.api.db.updateInterview).mock.calls[0][1]
      expect(payload).not.toHaveProperty('objectives')
      // Los objetivos de la sección fusionada no cambian
      const section = screen.getByTestId('objectives-section')
      expect(inputValues(section)).toEqual(['Objetivo A', 'Objetivo B'])
    })

    // SPEC-042 · AC-15 (los objetivos de una regeneración aparecen en la fusionada)
    it('shows the objectives produced by a script regeneration in the merged Objetivos section', async () => {
      const user = userEvent.setup()
      setInterview(WITH_SCRIPT)
      vi.mocked(mockApi.api.llm.generateScript).mockResolvedValue({
        ok: true,
        data: {
          ...WITH_SCRIPT,
          scriptMarkdown: '# Guión regenerado\nContenido nuevo',
          objectives: ['Objetivo regenerado']
        }
      })
      renderDetail()

      // OJO (lección SPEC-029): el botón se remonta al habilitarse — esperar
      // toBeEnabled por testid y re-consultar antes de clicar
      await waitFor(() => expect(screen.getByTestId('script-regenerate-button')).toBeEnabled())
      await user.click(screen.getByTestId('script-regenerate-button'))
      const dialog = await screen.findByRole('alertdialog')
      await user.click(within(dialog).getByRole('button', { name: 'Regenerar' }))

      const section = screen.getByTestId('objectives-section')
      await waitFor(() => expect(inputValues(section)).toEqual(['Objetivo regenerado']))
    })
  })
})
