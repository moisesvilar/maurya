/** Arranca la grabación de entrevista atravesando el consentimiento (SPEC-019). */
async function startRecording(user: ReturnType<typeof userEvent.setup>): Promise<void> {
  await user.click(await screen.findByRole('button', { name: 'Iniciar grabación' }))
  const consent = await screen.findByRole('alertdialog')
  await user.click(within(consent).getByRole('button', { name: 'Entendido, iniciar grabación' }))
  await screen.findByRole('button', { name: 'Detener' })
}

/** Arranca la captura desde el botón de la cabecera (SPEC-034) + consentimiento. */
async function startFromHeader(user: ReturnType<typeof userEvent.setup>): Promise<void> {
  await user.click(await screen.findByTestId('capture-start-button'))
  const consent = await screen.findByRole('alertdialog')
  await user.click(within(consent).getByRole('button', { name: 'Entendido, iniciar grabación' }))
  await screen.findByRole('button', { name: 'Detener' })
}

/** La sección «Grabación» (el <section> que envuelve su heading). */
async function grabacionSection(): Promise<HTMLElement> {
  const heading = await screen.findByRole('heading', { name: 'Grabación' })
  const section = heading.closest('section')
  if (section === null) {
    throw new Error('El heading «Grabación» debe vivir dentro de un <section>')
  }
  return section
}

/** a precede a b en el orden del documento (patrón SPEC-030). */
function expectBefore(a: HTMLElement, b: HTMLElement): void {
  expect(a.compareDocumentPosition(b) & Node.DOCUMENT_POSITION_FOLLOWING).toBeTruthy()
}

