describe('AssistantLiveSection (panel arriba SPEC-041)', () => {
  describe('interview detail', () => {
    // SPEC-041 · AC-01 (orden DOM: Objetivos → panel → Nota/Guión → Grabación)
    it('renders the assistant panel after the Objetivos section and before the Nota/Guión sections while recording', async () => {
      const user = userEvent.setup()
      renderInterviewDetail()
      await startRecording(user)

      const objetivos = await screen.findByRole('heading', { name: 'Objetivos', level: 3 })
      const panel = await screen.findByTestId('assistant-live-section')
      // Asíncrono (lección SPEC-029/030): el heading «Guión» solo renderiza
      // cuando NoteScriptSections resuelve getNoteByInterview
      const guion = await screen.findByRole('heading', { name: 'Guión' })
      const grabacion = await screen.findByRole('heading', { name: 'Grabación' })

      expectBefore(objetivos, panel)
      expectBefore(panel, guion)
      expectBefore(guion, grabacion)
    })

    // SPEC-041 · AC-02 (la Grabación ya no contiene el panel; lo demás permanece)
    it('keeps the Grabación section without the assistant panel while chronometer, Detener, meters and live transcript remain', async () => {
      const user = userEvent.setup()
      renderInterviewDetail()
      await startRecording(user)
      emitQueueItem()
      // El empty state del área en vivo solo se pinta con la transcripción activa
      act(() => {
        mockApi.emitTranscriptionStatus({ status: 'active' })
      })
      await screen.findByTestId('assistant-live-section')

      const section = await grabacionSection()
      // El panel y su cola ya NO viven dentro de la sección Grabación
      expect(within(section).queryByTestId('assistant-queue-item')).not.toBeInTheDocument()
      expect(within(section).queryByText(QUESTION)).not.toBeInTheDocument()
      // …sino en el contenedor de página de SPEC-041
      const panel = screen.getByTestId('assistant-live-section')
      expect(within(panel).getByText(QUESTION)).toBeInTheDocument()
      // Cronómetro, Detener, medidores y transcripción en vivo permanecen
      expect(within(section).getByText('00:00')).toBeInTheDocument()
      expect(within(section).getByRole('button', { name: 'Detener' })).toBeInTheDocument()
      expect(within(section).getAllByRole('progressbar')).toHaveLength(2)
      expect(within(section).getByLabelText('Nivel de Micrófono')).toBeInTheDocument()
      expect(within(section).getByLabelText('Nivel de Sistema')).toBeInTheDocument()
      expect(within(section).getByText('Esperando audio…')).toBeInTheDocument()
    })

    // SPEC-041 · AC-03 (preparación: el panel no existe en ninguna parte)
    it('does not render the assistant panel anywhere before the recording starts', async () => {
      renderInterviewDetail()

      expect(await screen.findByRole('button', { name: 'Iniciar grabación' })).toBeInTheDocument()
      expect(screen.queryByTestId('assistant-live-section')).not.toBeInTheDocument()
    })

    // SPEC-041 · AC-04 (estado Grabada: el panel no aparece)
    it('does not render the assistant panel on an already recorded interview', async () => {
      vi.mocked(mockApi.api.db.getInterview).mockResolvedValue({
        ok: true,
        data: interview({ wavPath: WAV_PATH, status: 'recorded' })
      })
      renderInterviewDetail()

      // Estado 3 — Grabada (resumen persistente con la ruta del WAV)
      expect(await screen.findByText(WAV_PATH)).toBeInTheDocument()
      expect(screen.queryByTestId('assistant-live-section')).not.toBeInTheDocument()
    })

    // SPEC-041 · AC-07 (el panel se renderiza igual que antes en la nueva ubicación)
    it('renders the queued question in the new location with its badges, question, reason and the SPEC-039 actions', async () => {
      const user = userEvent.setup()
      renderInterviewDetail()
      await startRecording(user)

      emitQueueItem()

      const panel = await screen.findByTestId('assistant-live-section')
      const item = within(panel).getByTestId('assistant-queue-item')
      expect(within(item).getByText('Profundiza')).toBeInTheDocument()
      expect(within(item).getByText('Genérico')).toBeInTheDocument()
      expect(within(item).getByText(QUESTION)).toBeInTheDocument()
      expect(within(item).getByText(REASON)).toBeInTheDocument()
      // Acciones por pregunta de SPEC-039, intactas
      expect(within(item).getByRole('button', { name: 'Marcar respondida' })).toBeInTheDocument()
      expect(within(item).getByRole('button', { name: 'Descartar pregunta' })).toBeInTheDocument()
      expect(within(item).getByRole('button', { name: 'Anclar pregunta' })).toBeInTheDocument()
    })

    // SPEC-041 · AC-08 (el cableado de las acciones no cambia)
    it('invokes the same assistant action from the new location when a question action is pressed', async () => {
      const user = userEvent.setup()
      renderInterviewDetail()
      await startRecording(user)
      emitQueueItem()
      const panel = await screen.findByTestId('assistant-live-section')
      const item = within(panel).getByTestId('assistant-queue-item')

      await user.click(within(item).getByRole('button', { name: 'Descartar pregunta' }))

      expect(vi.mocked(mockApi.api.assistant.resolveItem)).toHaveBeenCalledWith('q-1', 'discarded')
    })

    // SPEC-041 · AC-09 (regresión SPEC-039: el Dialog de motivos se abre igual)
    it('still opens the Preguntas descartadas dialog after a stop with discarded questions', async () => {
      const user = userEvent.setup()
      vi.mocked(mockApi.api.recording.stop).mockResolvedValue(
        stopResult({
          interview: interview({
            wavPath: WAV_PATH,
            status: 'recorded',
            questionOutcomes: [{ question: QUESTION, outcome: 'discarded' }]
          })
        })
      )
      renderInterviewDetail()
      await startRecording(user)

      await user.click(screen.getByRole('button', { name: 'Detener' }))

      const dialog = await screen.findByTestId('discard-reasons-dialog')
      expect(
        within(dialog).getByRole('heading', { name: 'Preguntas descartadas' })
      ).toBeInTheDocument()
      expect(within(dialog).getByText(QUESTION)).toBeInTheDocument()
      expect(within(dialog).getAllByTestId('discard-reason-input')).toHaveLength(1)
    })

    // SPEC-041 · AC-10 (regresión SPEC-035: la transcripción en vivo sigue SOLO
    // en la Grabación de la entrevista; el lado captura lo cubre la suite de
    // SPEC-035, verde tras la adaptación posicional)
    it('keeps the live transcript area rendering inside the interview Grabación section only', async () => {
      const user = userEvent.setup()
      renderInterviewDetail()
      await startRecording(user)

      act(() => {
        mockApi.emitTranscriptionStatus({ status: 'active' })
        mockApi.emitTranscriptionResult({
          channel: 'mic',
          text: LIVE_LINE_TEXT,
          startMs: 1000,
          endMs: 2600,
          receivedAtMs: 2700,
          isFinal: true,
          speaker: 0,
          offsetSeconds: 1
        })
      })

      const section = await grabacionSection()
      expect(await within(section).findByText(LIVE_LINE_TEXT)).toBeInTheDocument()
      // La línea en vivo no se cuela en el panel de arriba
      const panel = screen.getByTestId('assistant-live-section')
      expect(within(panel).queryByText(LIVE_LINE_TEXT)).not.toBeInTheDocument()
    })
  })

  describe('capture detail', () => {
    // SPEC-041 · AC-05 (captura grabando: el panel encima de Nota/Guión)
    it('renders the assistant panel right above the Nota/Guión sections while recording a capture', async () => {
      const user = userEvent.setup()
      renderCaptureDetail()
      await startFromHeader(user)

      const title = await screen.findByRole('heading', { name: 'Captura sin empresa', level: 1 })
      const panel = await screen.findByTestId('assistant-live-section')
      const guion = await screen.findByRole('heading', { name: 'Guión' })

      // Sin sección Objetivos en la captura: cabecera → panel → Nota/Guión.
      // La sección «Grabación» ya no cierra la página mientras se graba: la
      // sesión en vivo vive en la top bar (extensión de SPEC-034).
      expect(screen.queryByRole('heading', { name: 'Objetivos', level: 3 })).not.toBeInTheDocument()
      expect(screen.queryByRole('heading', { name: 'Grabación' })).not.toBeInTheDocument()
      expectBefore(title, panel)
      expectBefore(panel, guion)
    })

    // SPEC-041 · AC-06 (captura sin grabar: el panel no aparece)
    it('does not render the assistant panel on the capture detail without a recording in progress', async () => {
      renderCaptureDetail()

      expect(await screen.findByTestId('capture-start-button')).toBeInTheDocument()
      expect(screen.queryByTestId('assistant-live-section')).not.toBeInTheDocument()
    })
  })
})
