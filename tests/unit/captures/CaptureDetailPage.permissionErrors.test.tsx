/** La sección «Grabación» (el <section> que envuelve su heading). */
async function grabacionSection(): Promise<HTMLElement> {
  const heading = await screen.findByRole('heading', { name: 'Grabación' })
  const section = heading.closest('section')
  if (section === null) {
    throw new Error('El heading «Grabación» debe vivir dentro de un <section>')
  }
  return section
}

/**
 * Arranca desde el botón de la cabecera atravesando el aviso de consentimiento
 * (SPEC-019) sin esperar resultado: el desenlace (Detener o Alert de permiso)
 * lo asevera cada test.
 */
async function startFromHeader(user: ReturnType<typeof userEvent.setup>): Promise<void> {
  await user.click(await screen.findByTestId('capture-start-button'))
  const consent = await screen.findByRole('alertdialog')
  expect(within(consent).getByRole('heading', { name: 'Aviso de grabación' })).toBeInTheDocument()
  await user.click(within(consent).getByRole('button', { name: 'Entendido, iniciar grabación' }))
}

