      vi.setSystemTime(BASE_TIME_MS + MIN_INTERVAL_MS + 1000)
      feedFinalLines(3)
      await waitForCreateCalls(2)

      const firstSystem = systemOfCall(0)
      const secondSystem = systemOfCall(1)
      expect(JSON.stringify(secondSystem)).toBe(JSON.stringify(firstSystem))
      const secondBlocks = secondSystem as Array<{ text: string }>
      expect(secondBlocks[0].text).not.toContain('Nueva persona en caliente.')

      // La SIGUIENTE sesión sí arranca con el override
      stopAssistant()
      const next = createSender()
      startAssistant(next.sender, interviewId)
      feedFinalLines(3)
      await waitForCreateCalls(3)

      const thirdBlocks = systemOfCall(2) as Array<{ text: string }>
      // Adaptado a SPEC-031: la persona viaja delimitada, ya no abre el bloque
      expect(personaBlockContent(thirdBlocks[0].text)).toBe('Nueva persona en caliente.')
