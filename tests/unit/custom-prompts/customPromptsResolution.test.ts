function createSender(): { sender: WebContents; send: ReturnType<typeof vi.fn> } {
  const send = vi.fn()
  const senderLike = { isDestroyed: (): boolean => false, send }
  return { sender: senderLike as unknown as WebContents, send }
}

function assistantEvents(send: ReturnType<typeof vi.fn>): AssistantUpdateEvent[] {
  return send.mock.calls
    .filter((call) => call[0] === 'assistant:update')
    .map((call) => call[1] as AssistantUpdateEvent)
}

let lineCounter = 0

/** Inyecta N líneas finales por el listener capturado (dispara el análisis). */
function feedFinalLines(count: number): void {
  for (let index = 0; index < count; index += 1) {
    lineCounter += 1
    harness.finalLineListener?.({
      channel: lineCounter % 2 === 0 ? 'system' : 'mic',
      speaker: 0,
      text: `Línea de conversación ${lineCounter}`,
      startMs: lineCounter * 1000,
      endMs: lineCounter * 1000 + 900,
      receivedAtMs: lineCounter * 1000 + 950
    })
  }
}

async function waitForCreateCalls(count: number): Promise<void> {
  await vi.waitFor(() => expect(harness.create).toHaveBeenCalledTimes(count))
}

/** Espera a que el último evento sea 'active' (la promesa del análisis resolvió). */
async function waitForActive(send: ReturnType<typeof vi.fn>): Promise<void> {
  await vi.waitFor(() => {
    expect(assistantEvents(send).at(-1)?.state).toBe('active')
  })
}

/** El system de la llamada N (0-based) del SDK, como lo recibió messages.create. */
function systemOfCall(index: number): unknown {
  const params = harness.create.mock.calls[index][0] as Record<string, unknown>
  return params.system
}

/**
 * Índices de línea de los delimitadores del bloque de persona (SPEC-031).
 * Línea EXACTA: la salvaguarda menciona los delimitadores entre comillas
 * dentro de una línea más larga, así que indexOf sobre el string los
 * encontraría antes de tiempo. Falla si falta algún delimitador.
 */
function personaBlockBounds(system: string): { lines: string[]; start: number; end: number } {
  const lines = system.split('\n')
  const start = lines.indexOf(PERSONA_BLOCK_START)
  const end = lines.indexOf(PERSONA_BLOCK_END)
  expect(start).toBeGreaterThanOrEqual(0)
  expect(end).toBeGreaterThan(start)
  return { lines, start, end }
}

/** Contenido del bloque de persona delimitado (entre START y END). */
function personaBlockContent(system: string): string {
  const { lines, start, end } = personaBlockBounds(system)
  return lines
    .slice(start + 1, end)
    .join('\n')
    .trim()
}

