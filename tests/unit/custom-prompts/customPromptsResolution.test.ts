      const { sender, send } = createSender()

      startAssistant(sender, interviewId)
      feedFinalLines(3)
      await waitForCreateCalls(1)
      await waitForActive(send)

      // Override guardado A MITAD de sesión: no debe tocar el prefijo en curso
