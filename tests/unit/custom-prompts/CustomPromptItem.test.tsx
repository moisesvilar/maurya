/**
 * SPEC-031: Prompts personalizados en acordeón — listado colapsado, edición
 * in-place con el editor Markdown WYSIWYG y restablecer con resincronización
 * (AC-01..AC-13). Migra aquí la intención vigente de la edición de SPEC-026
 * (AC-06..AC-13, antes en CustomPromptSheet.test.tsx, retirado: el Sheet quedó
 * derogado por SPEC-031). Frontera de mocking: api.db del bridge; el editor
 * TipTap se monta REAL sobre jsdom (polyfills en tests/helpers/prosemirrorJsdom.ts).
 *
 * Nota de estrategia: para "modificar el texto" los tests usan la acción
 * Título de la botonera (cambio de documento determinista); el vaciado usa los
 * atajos que ProseMirror implementa como keydown handlers (Ctrl+A + Backspace).
 * Simular escritura libre en un contenteditable de ProseMirror no es fiable en
 * jsdom (beforeinput no implementado).
 * Generated by QA Dev agent.
 */
import { render, screen, waitFor, within, type RenderResult } from '@testing-library/react'
import userEvent from '@testing-library/user-event'
import { beforeAll, beforeEach, describe, expect, it, vi } from 'vitest'
import { MemoryRouter, Route, Routes } from 'react-router-dom'
import { Toaster } from '@/components/ui/sonner'
import { TooltipProvider } from '@/components/ui/tooltip'
import { SettingsPage } from '@/pages/SettingsPage'
import type { CustomPrompt, CustomPromptId } from '@/types/domain'
import { installMockApi, type MockApiHandle } from '../../helpers/mockApi'
import { installProseMirrorJsdomPolyfills } from '../../helpers/prosemirrorJsdom'

let mockApi: MockApiHandle

/** Vistas del catálogo con la forma del bridge; los textos son fixtures. */
const DEFAULT_BODIES: Record<CustomPromptId, string> = {
  script: 'Eres un preparador experto de entrevistas de discovery.',
  note: 'Eres un sintetizador experto de entrevistas de discovery.',
  assistant: 'Eres el copiloto en tiempo real de un entrevistador.'
}

function promptView(id: CustomPromptId, overrides: Partial<CustomPrompt> = {}): CustomPrompt {
  return {
    id,
    defaultBody: DEFAULT_BODIES[id],
    lockedRules: `Reglas fijas de ${id}\n- Responde únicamente con el JSON pedido.`,
    overrideBody: null,
    updatedAt: null,
    ...overrides
  }
}

function allDefaults(): CustomPrompt[] {
  return [promptView('script'), promptView('note'), promptView('assistant')]
}

function setPrompts(prompts: CustomPrompt[]): void {
  vi.mocked(mockApi.api.db.listCustomPrompts).mockResolvedValue({ ok: true, data: prompts })
}

function renderSettings(): RenderResult {
  return render(
    <TooltipProvider>
      <MemoryRouter initialEntries={['/settings?tab=custom-prompts']}>
        <Routes>
          <Route path="/settings" element={<SettingsPage />} />
        </Routes>
      </MemoryRouter>
      <Toaster />
    </TooltipProvider>
  )
}

/** Expande el ítem del acordeón con su lápiz y devuelve el panel in-place. */
async function expandItem(
  user: ReturnType<typeof userEvent.setup>,
  id: CustomPromptId = 'script'
): Promise<HTMLElement> {
  const row = await screen.findByTestId(`custom-prompt-row-${id}`)
  await user.click(within(row).getByRole('button', { name: 'Editar prompt' }))
  return await screen.findByTestId(`custom-prompt-panel-${id}`)
}

/** Cambio de documento determinista: alterna Título desde la botonera del panel. */
async function toggleHeading(
  user: ReturnType<typeof userEvent.setup>,
  panel: HTMLElement
): Promise<void> {
  await user.click(within(panel).getByRole('button', { name: 'Título' }))
}

beforeAll(() => {
  installProseMirrorJsdomPolyfills()
})

beforeEach(() => {
  vi.clearAllMocks()
  mockApi = installMockApi()
  setPrompts(allDefaults())
})

describe('CustomPromptItem', () => {
  describe('accordion listing', () => {
    // SPEC-031 · AC-01
    it('renders the three prompts collapsed with name, description, badge, pencil and reset buttons', async () => {
      renderSettings()

      expect(await screen.findByTestId('custom-prompts-list')).toBeInTheDocument()
      const rows: Array<[CustomPromptId, string, string]> = [
        [
          'script',
          'Guión y objetivos',
          'Prepara el guión personalizado y los objetivos de cada entrevista'
        ],
        ['note', 'Nota de resumen', 'Sintetiza la nota de resumen al cerrar la entrevista'],
        ['assistant', 'Asistente en vivo', 'Sugiere la siguiente jugada durante la llamada']
      ]
      for (const [id, name, description] of rows) {
        const row = screen.getByTestId(`custom-prompt-row-${id}`)
        expect(within(row).getByText(name)).toBeInTheDocument()
        expect(within(row).getByText(description)).toBeInTheDocument()
        expect(within(row).getByText(/^(Default|Personalizado)$/)).toBeInTheDocument()
        const pencil = within(row).getByRole('button', { name: 'Editar prompt' })
        expect(pencil).toHaveAttribute('aria-expanded', 'false')
        expect(within(row).getByRole('button', { name: 'Restablecer prompt' })).toBeInTheDocument()
        // Colapsado: el panel in-place no está montado
        expect(screen.queryByTestId(`custom-prompt-panel-${id}`)).not.toBeInTheDocument()
      }
    })

    // SPEC-031 · AC-02
    it('does not render any locked-rules section nor a lateral Sheet anywhere in the tab', async () => {
      const user = userEvent.setup()
      renderSettings()

      await screen.findByTestId('custom-prompts-list')
      expect(screen.queryByTestId('custom-prompt-sheet')).not.toBeInTheDocument()
      expect(screen.queryByTestId('custom-prompt-locked-rules')).not.toBeInTheDocument()
      expect(screen.queryByText(/Reglas fijas/)).not.toBeInTheDocument()
      expect(screen.queryByRole('dialog')).not.toBeInTheDocument()

      // Tampoco al expandir un ítem: las reglas bloqueadas ya no se visualizan
      await expandItem(user, 'script')
      expect(screen.queryByTestId('custom-prompt-locked-rules')).not.toBeInTheDocument()
      expect(screen.queryByText(/Reglas fijas/)).not.toBeInTheDocument()
      expect(
        screen.queryByText('- Responde únicamente con el JSON pedido.')
      ).not.toBeInTheDocument()
      expect(screen.queryByTestId('custom-prompt-sheet')).not.toBeInTheDocument()
    })

    // SPEC-031 · AC-03
    it('expands the item in-place on pencil click showing the WYSIWYG editor with the current text and its toolbar', async () => {
      const user = userEvent.setup()
      renderSettings()

      const panel = await expandItem(user, 'script')

      const row = screen.getByTestId('custom-prompt-row-script')
      expect(within(row).getByRole('button', { name: 'Editar prompt' })).toHaveAttribute(
        'aria-expanded',
        'true'
      )
      const editor = within(panel).getByTestId('custom-prompt-editor')
      expect(
        within(editor).getByText(/Eres un preparador experto de entrevistas de discovery\./)
      ).toBeInTheDocument()
      // Botonera del editor vigente de SPEC-026 (5 acciones de formato)
      const toolbar = within(panel).getByTestId('custom-prompt-editor-toolbar')
      for (const label of ['Negrita', 'Cursiva', 'Título', 'Lista de viñetas', 'Lista numerada']) {
        expect(within(toolbar).getByRole('button', { name: label })).toBeInTheDocument()
      }
    })

    // SPEC-031 · AC-04
    it('keeps the first item expanded when expanding a second one (multiple expansion)', async () => {
      const user = userEvent.setup()
      renderSettings()

      await expandItem(user, 'script')
      await expandItem(user, 'note')

      expect(screen.getByTestId('custom-prompt-panel-script')).toBeInTheDocument()
      expect(screen.getByTestId('custom-prompt-panel-note')).toBeInTheDocument()
      const noteEditor = within(screen.getByTestId('custom-prompt-panel-note')).getByTestId(
        'custom-prompt-editor'
      )
      expect(
        within(noteEditor).getByText(/Eres un sintetizador experto de entrevistas de discovery\./)
      ).toBeInTheDocument()
    })

    // SPEC-031 · AC-05
    it('collapses the item directly on pencil click when there are no unsaved changes', async () => {
      const user = userEvent.setup()
      renderSettings()

      await expandItem(user, 'script')
      const row = screen.getByTestId('custom-prompt-row-script')
      await user.click(within(row).getByRole('button', { name: 'Editar prompt' }))

      expect(screen.queryByTestId('custom-prompt-panel-script')).not.toBeInTheDocument()
      expect(screen.queryByRole('alertdialog')).not.toBeInTheDocument()
      expect(within(row).getByRole('button', { name: 'Editar prompt' })).toHaveAttribute(
        'aria-expanded',
        'false'
      )
    })
  })

  describe('in-place editing', () => {
    // SPEC-031 · AC-06
    it('shows the Guardar and Descartar buttons only after the editor content changes', async () => {
      const user = userEvent.setup()
      renderSettings()

      const panel = await expandItem(user, 'script')
      expect(screen.queryByTestId('custom-prompt-actions-script')).not.toBeInTheDocument()

      await toggleHeading(user, panel)

      const actions = await screen.findByTestId('custom-prompt-actions-script')
      expect(within(actions).getByRole('button', { name: 'Guardar' })).toBeInTheDocument()
      expect(within(actions).getByRole('button', { name: 'Descartar' })).toBeInTheDocument()
    })

    // SPEC-031 · AC-07 (migra SPEC-026 AC-08 sin la parte derogada «el Sheet se cierra»)
    it('saves the text as Markdown, shows the "Prompt guardado" toast, switches the badge to "Personalizado" and keeps the item expanded without the buttons', async () => {
      const user = userEvent.setup()
      vi.mocked(mockApi.api.db.saveCustomPrompt).mockImplementation(async (id, body) => ({
        ok: true,
        data: promptView(id, { overrideBody: body, updatedAt: '2026-07-12T10:00:00.000Z' })
      }))
      renderSettings()

      const panel = await expandItem(user, 'script')
      await toggleHeading(user, panel)
      const actions = await screen.findByTestId('custom-prompt-actions-script')
      await user.click(within(actions).getByRole('button', { name: 'Guardar' }))

      // Se persiste el Markdown plano serializado (el título → prefijo '## ')
      await waitFor(() =>
        expect(vi.mocked(mockApi.api.db.saveCustomPrompt)).toHaveBeenCalledWith(
          'script',
          expect.stringMatching(/^## Eres un preparador experto/)
        )
      )
      const toasts = await screen.findAllByText('Prompt guardado')
      expect(toasts.length).toBeGreaterThanOrEqual(1)
      const row = screen.getByTestId('custom-prompt-row-script')
      expect(await within(row).findByText('Personalizado')).toBeInTheDocument()
      // Los botones desaparecen (ya no hay cambios) y el ítem sigue expandido
      await waitFor(() =>
        expect(screen.queryByTestId('custom-prompt-actions-script')).not.toBeInTheDocument()
      )
      const editor = within(screen.getByTestId('custom-prompt-panel-script')).getByTestId(
        'custom-prompt-editor'
      )
      // Sin remontaje: el editor conserva el documento con su formato
      expect(within(editor).getByRole('heading', { level: 2 })).toHaveTextContent(
        'Eres un preparador experto de entrevistas de discovery.'
      )
    })

    // SPEC-031 · AC-08 (vaciado vía atajos de ProseMirror: Ctrl+A + Backspace)
    it('shows the inline error "El prompt no puede quedar vacío" and does not persist when saving an emptied editor', async () => {
      const user = userEvent.setup()
      renderSettings()

      const panel = await expandItem(user, 'script')
      within(panel).getByLabelText('Persona y enfoque').focus()
      await user.keyboard('{Control>}a{/Control}{Backspace}')
      await waitFor(() =>
        expect(
          within(panel).queryByText(/Eres un preparador experto de entrevistas de discovery\./)
        ).not.toBeInTheDocument()
      )
      const actions = await screen.findByTestId('custom-prompt-actions-script')
      await user.click(within(actions).getByRole('button', { name: 'Guardar' }))

      expect(await within(panel).findByText('El prompt no puede quedar vacío')).toBeInTheDocument()
      expect(vi.mocked(mockApi.api.db.saveCustomPrompt)).not.toHaveBeenCalled()
      expect(screen.getByTestId('custom-prompt-panel-script')).toBeInTheDocument()
    })

    // SPEC-031 · AC-09
    it('restores the current text and keeps the item expanded after confirming in the "Descartar cambios" AlertDialog', async () => {
      const user = userEvent.setup()
      renderSettings()

      const panel = await expandItem(user, 'script')
      await toggleHeading(user, panel)
      const actions = await screen.findByTestId('custom-prompt-actions-script')
      await user.click(within(actions).getByRole('button', { name: 'Descartar' }))

      const dialog = await screen.findByRole('alertdialog')
      expect(within(dialog).getByRole('heading', { name: 'Descartar cambios' })).toBeInTheDocument()
      expect(
        within(dialog).getByText('Los cambios no guardados en el prompt se perderán.')
      ).toBeInTheDocument()
      await user.click(within(dialog).getByRole('button', { name: 'Descartar' }))

      // El editor restaura el texto vigente (el título desaparece), los botones
      // se van y el ítem permanece expandido; nada se persistió
      const refreshedPanel = screen.getByTestId('custom-prompt-panel-script')
      const editor = within(refreshedPanel).getByTestId('custom-prompt-editor')
      await waitFor(() =>
        expect(within(editor).queryByRole('heading', { level: 2 })).not.toBeInTheDocument()
      )
      expect(
        within(editor).getByText(/Eres un preparador experto de entrevistas de discovery\./)
      ).toBeInTheDocument()
      expect(screen.queryByTestId('custom-prompt-actions-script')).not.toBeInTheDocument()
      expect(vi.mocked(mockApi.api.db.saveCustomPrompt)).not.toHaveBeenCalled()
    })

    // SPEC-031 · AC-10
    it('asks to discard before collapsing with unsaved changes, keeping them on cancel and collapsing on confirm', async () => {
      const user = userEvent.setup()
      renderSettings()

      const panel = await expandItem(user, 'script')
      await toggleHeading(user, panel)
      await screen.findByTestId('custom-prompt-actions-script')
      const row = screen.getByTestId('custom-prompt-row-script')

      // Cancelar: permanece expandido y conserva los cambios
      await user.click(within(row).getByRole('button', { name: 'Editar prompt' }))
      const firstDialog = await screen.findByRole('alertdialog')
      expect(
        within(firstDialog).getByRole('heading', { name: 'Descartar cambios' })
      ).toBeInTheDocument()
      await user.click(within(firstDialog).getByRole('button', { name: 'Cancelar' }))

      await waitFor(() => expect(screen.queryByRole('alertdialog')).not.toBeInTheDocument())
      const keptPanel = screen.getByTestId('custom-prompt-panel-script')
      expect(
        within(keptPanel).getByRole('heading', { level: 2, name: /Eres un preparador experto/ })
      ).toBeInTheDocument()
      expect(screen.getByTestId('custom-prompt-actions-script')).toBeInTheDocument()

      // Confirmar: descarta y colapsa
      await user.click(within(row).getByRole('button', { name: 'Editar prompt' }))
      const secondDialog = await screen.findByRole('alertdialog')
      await user.click(within(secondDialog).getByRole('button', { name: 'Descartar' }))

      await waitFor(() =>
        expect(screen.queryByTestId('custom-prompt-panel-script')).not.toBeInTheDocument()
      )
      expect(vi.mocked(mockApi.api.db.saveCustomPrompt)).not.toHaveBeenCalled()
    })

    // SPEC-031 · AC-11 (migra SPEC-026 AC-11 sobre el acordeón)
    it('keeps the written text with the buttons visible and shows a destructive toast when saving fails', async () => {
      const user = userEvent.setup()
      vi.mocked(mockApi.api.db.saveCustomPrompt).mockResolvedValue({
        ok: false,
        error: { kind: 'storage', message: 'Fallo simulado al guardar el prompt' }
      })
      renderSettings()

      const panel = await expandItem(user, 'script')
      await toggleHeading(user, panel)
      const actions = await screen.findByTestId('custom-prompt-actions-script')
      await user.click(within(actions).getByRole('button', { name: 'Guardar' }))

      const toasts = await screen.findAllByText('Fallo simulado al guardar el prompt')
      expect(toasts.length).toBeGreaterThanOrEqual(1)
      // El ítem sigue expandido, el editor conserva el texto con su formato y
      // la barra Guardar/Descartar sigue visible
      const keptPanel = screen.getByTestId('custom-prompt-panel-script')
      expect(
        within(keptPanel).getByRole('heading', { level: 2, name: /Eres un preparador experto/ })
      ).toBeInTheDocument()
      expect(screen.getByTestId('custom-prompt-actions-script')).toBeInTheDocument()
    })

    // SPEC-031 · AC-12 (migra SPEC-026 AC-09: round-trip fiel del formato)
    it('renders a saved override with its exact formatting when expanding the item (Markdown round-trip)', async () => {
      const user = userEvent.setup()
      setPrompts([
        promptView('script', {
          overrideBody: '**Negrita** y una lista:\n\n- uno\n- dos',
          updatedAt: '2026-07-10T10:00:00.000Z'
        }),
        promptView('note'),
        promptView('assistant')
      ])
      renderSettings()

      const panel = await expandItem(user, 'script')

      const editor = within(panel).getByTestId('custom-prompt-editor')
      const bold = within(editor).getByText('Negrita')
      expect(bold.tagName).toBe('STRONG')
      const items = within(editor).getAllByRole('listitem')
      expect(items.map((item) => item.textContent)).toEqual(['uno', 'dos'])
      // El default NO se muestra: manda el override
      expect(within(editor).queryByText(/Eres un preparador experto/)).not.toBeInTheDocument()
    })
  })

  describe('formatting from the toolbar', () => {
    // SPEC-026 · AC-07 (vigente, migrado del Sheet al ítem del acordeón)
    it('applies toolbar formatting and reflects it immediately in the editor view', async () => {
      const user = userEvent.setup()
      renderSettings()

      const panel = await expandItem(user, 'script')
      const editor = within(panel).getByTestId('custom-prompt-editor')
      expect(within(editor).queryByRole('heading')).not.toBeInTheDocument()

      await toggleHeading(user, panel)

      const heading = await within(editor).findByRole('heading', { level: 2 })
      expect(heading).toHaveTextContent('Eres un preparador experto de entrevistas de discovery.')
      // La acción activa se refleja en la botonera (aria-pressed)
      expect(within(panel).getByRole('button', { name: 'Título' })).toHaveAttribute(
        'aria-pressed',
        'true'
      )
    })
  })

  describe('resetting an expanded item', () => {
    // SPEC-031 · AC-13
    it('resets to default, switches the badge, shows the toast and resyncs the expanded editor to the default text without pending changes', async () => {
      const user = userEvent.setup()
      setPrompts([
        promptView('script', {
          overrideBody: '**Persona propia** del guión.',
          updatedAt: '2026-07-10T10:00:00.000Z'
        }),
        promptView('note'),
        promptView('assistant')
      ])
      vi.mocked(mockApi.api.db.resetCustomPrompt).mockResolvedValue({
        ok: true,
        data: promptView('script')
      })
      renderSettings()

      const panel = await expandItem(user, 'script')
      expect(within(panel).getByText('Persona propia').tagName).toBe('STRONG')

      const row = screen.getByTestId('custom-prompt-row-script')
      await user.click(within(row).getByRole('button', { name: 'Restablecer prompt' }))
      const dialog = await screen.findByRole('alertdialog')
      expect(
        within(dialog).getByRole('heading', { name: 'Restablecer prompt' })
      ).toBeInTheDocument()
      await user.click(within(dialog).getByRole('button', { name: 'Restablecer' }))

      expect(vi.mocked(mockApi.api.db.resetCustomPrompt)).toHaveBeenCalledWith('script')
      expect(await within(row).findByText('Default')).toBeInTheDocument()
      expect(within(row).queryByText('Personalizado')).not.toBeInTheDocument()
      const toasts = await screen.findAllByText('Prompt restablecido')
      expect(toasts.length).toBeGreaterThanOrEqual(1)
      // El editor expandido resincroniza al texto default, sin cambios pendientes
      const refreshedPanel = screen.getByTestId('custom-prompt-panel-script')
      const editor = within(refreshedPanel).getByTestId('custom-prompt-editor')
      await waitFor(() =>
        expect(
          within(editor).getByText(/Eres un preparador experto de entrevistas de discovery\./)
        ).toBeInTheDocument()
      )
      expect(within(editor).queryByText('Persona propia')).not.toBeInTheDocument()
      expect(screen.queryByTestId('custom-prompt-actions-script')).not.toBeInTheDocument()
    })
  })
})
